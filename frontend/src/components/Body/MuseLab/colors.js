export default colorPicker = (num) => {
  switch (num) {
    case 1:
      return '#ffadad'
    case 2:
      return '#ffd6a5'
    case 3:
      return '#fdffb6'
    case 4:
      return '#caffbf'
    case 5:
      return '#9bf6ff'
    case 6:
      return '#a0c4ff'
    case 7:
      return '#bdb2ff'
    case 8:
      return '#ffc6ff'
  }
}
